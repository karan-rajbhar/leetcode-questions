# @lc code=start
# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def middleNode(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        current=head
        while current and head and head.next:
            current=current.next
            head=head.next.next
        
        return current
# @lc code=end

